# FIND pesquisará dentro de uma string, o texto que for pedido e mostrará aonde 
# se inicia o texto, por exemplo, abaixo, o 'no' se inicia no caractere 19.
# Lembrando que mostra apenas aonde se inicia, e não o começo e fim.
# Quando FIND te retornar -1, o texto não existe na str

frase = 'João botou o melão no gol.'
print(frase.find('no'))